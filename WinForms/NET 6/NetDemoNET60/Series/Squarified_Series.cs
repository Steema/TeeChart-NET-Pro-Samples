	    // Returns the two parts of a string (text and value), eg:  abc 123
	    private bool GetTextAndValue(ref string s, out string aText, out double aValue)
	    {
		    int i = 0;
		    string tmp = "";

		    bool result = false;

		    i = s.IndexOf(' ');

		    if (i > 0)
		    {
			    aText = s.Substring(0, i).Trim();
			    tmp = s.Substring(i + 1).Trim();

			    result = double.TryParse(tmp, out aValue);
		    }
		    else
		    {
			    aText = "";
			    aValue = 0;
		    }

		    return result;
	    }


	    private void AddText(List<string> lines)
	    {
		    foreach (var line in lines)
		    {
			    if (GetTextAndValue(line, out string tmpText, out double tmpValue))
			    {
                    series1.AddItem(0, tmpValue, tmpText);
			    }
		    }
	    }

